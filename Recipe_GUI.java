    private void createCategoryNRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createCategoryNRActionPerformed
        String catName = recipeNewCategoryNR.getText();
        int i=0;
        Boolean alreadyExist = false;
        while(i < Recipes.ALL.size()){
            if(Recipes.ALL.get(i).nameCategory.compareTo(catName) == 0)
                alreadyExist = true;
            i++;
        }
        if(alreadyExist){
            ImageIcon minionEvilAngry = new ImageIcon(TestOption.class.getResource("/recipes/minionEvilAngry.png"));
            JOptionPane.showMessageDialog(null, "A categoria que deseja criar já existe!", "Aviso de Insucesso :(", 1, minionEvilAngry);
            recipeNewCategoryNR.setText("");
        }
        else{
            if(catName.contains(";")){
                ImageIcon minionHumf = new ImageIcon(TestOption.class.getResource("/recipes/minionHumf.png"));
                JOptionPane.showMessageDialog(null, "Um dos campos tem presente o caracter \";\".\nDeve remover esse caracter!", "Aviso de Insucesso :(", 1, minionHumf);
            } 
            else{
                if(!catName.isEmpty()){
                    Recipes.createCategory(catName);
                    Recipes.createCategoryFile(catName);
                    ImageIcon minionFruit = new ImageIcon(TestOption.class.getResource("/recipes/minionFruit.png"));
                    JOptionPane.showMessageDialog(null, "A categoria \"" + catName + "\" foi criada com sucesso!", "Aviso de Sucesso :)", 1, minionFruit);
                    recipeNewCategoryNR.setText("");
                    categoryListNR.setListData(Recipes.listOfCategories().toArray());  
                    categoryListNR.setSelectedIndex(Recipes.listOfCategories().indexOf(catName));
                }
                else{
                    ImageIcon minionPlease = new ImageIcon(TestOption.class.getResource("/recipes/minionPlease.png"));
                    JOptionPane.showMessageDialog(null, "Insere o nome da categoria que deseja criar!", "Aviso de Insucesso :(", 1, minionPlease);
                }
            }  
        }
    }//GEN-LAST:event_createCategoryNRActionPerformed

    private void createRecipeNRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createRecipeNRActionPerformed
        int catIndice = categoryListNR.getSelectedIndex();
        if(catIndice != -1){
            
            Boolean nam = recipeNameNR.getText().isEmpty();
            Boolean com = recipeCommentNR.getText().isEmpty();
            Boolean ing = recipeIngredientsNR.getText().isEmpty();
            Boolean pre = recipePreparationNR.getText().isEmpty();

            if(nam){
                ImageIcon minionMaid = new ImageIcon(TestOption.class.getResource("/recipes/minionMaid.png"));
                JOptionPane.showMessageDialog(null, "Insere o nome da receita que deseja criar!", "Aviso de Insucesso :(", 1, minionMaid);
            }
            else {
                Boolean alreadyExist = false;
                int i = 0;
                String recName = recipeNameNR.getText();
                while(i < Recipes.ALL.get(catIndice).recipesCategory.size()){
                    if(Recipes.ALL.get(catIndice).recipesCategory.get(i).nameRecipe.compareTo(recName) == 0)
                        alreadyExist = true;
                i++;
                }
                if (alreadyExist){
                    ImageIcon minionAgnesAngry = new ImageIcon(TestOption.class.getResource("/recipes/minionAgnesAngry.png"));
                    JOptionPane.showMessageDialog(null, "O nome da receita que está a tentar utilizar já se encontra em uso!", "Aviso de Insucesso :(", 1, minionAgnesAngry);
                    recipeNameNR.setText("");
                }
                else{
                    if(recipeNameNR.getText().contains(";") || recipeCommentNR.getText().contains(";") || recipeIngredientsNR.getText().contains(";") || recipePreparationNR.getText().contains(";")){
                        ImageIcon minionHumf = new ImageIcon(TestOption.class.getResource("/recipes/minionHumf.png"));
                        JOptionPane.showMessageDialog(null, "Um dos campos tem presente o caracter \";\".\nDeve remover esse caracter!", "Aviso de Insucesso :(", 1, minionHumf);
                    }
                    else{    
                        if(com || ing || pre){ 
                            Object[] options = { "Sim", "Não" };
                            ImageIcon minionAlarm = new ImageIcon(TestOption.class.getResource("/recipes/minionAlarm.png"));
                            int option = JOptionPane.showOptionDialog(null, "Existem campos por preencher! Pretende criar ainda assim a receita?", "Aviso de campos vazios :/", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, minionAlarm, options, options[0]);

                            switch(option){
                                case 0:
                                    try {
                                        Recipes.createRecipe(catIndice, recipeNameNR.getText(), recipeCommentNR.getText(), recipeIngredientsNR.getText(), recipePreparationNR.getText());
                                        Recipes.saveRecipeFile(Recipes.ALL.get(catIndice).nameCategory, recipeNameNR.getText(), recipeCommentNR.getText(), recipeIngredientsNR.getText(), recipePreparationNR.getText());
                                        ImageIcon minionBanana = new ImageIcon(TestOption.class.getResource("/recipes/minionBanana.png"));
                                        JOptionPane.showMessageDialog(null, "A receita \"" + recipeNameNR.getText() + "\" foi criada com sucesso!", "Aviso de Sucesso :)", 1, minionBanana);
                                        recipeNameNR.setText("");
                                        recipeCommentNR.setText("");
                                        recipeIngredientsNR.setText("");
                                        recipePreparationNR.setText("");
                                    } catch (IOException ex) {
                                        Logger.getLogger(Recipe_GUI.class.getName()).log(Level.SEVERE, null, ex);
                                    }
                                    break;       
                            }
                        }
                        else{
                            try {
                                Recipes.createRecipe(catIndice, recipeNameNR.getText(), recipeCommentNR.getText(), recipeIngredientsNR.getText(), recipePreparationNR.getText());
                                Recipes.saveRecipeFile(Recipes.ALL.get(catIndice).nameCategory, recipeNameNR.getText(), recipeCommentNR.getText(), recipeIngredientsNR.getText(), recipePreparationNR.getText());
                                ImageIcon minionBanana = new ImageIcon(TestOption.class.getResource("/recipes/minionBanana.png"));
                                JOptionPane.showMessageDialog(null, "A receita \"" + recipeNameNR.getText() + "\" foi criada com sucesso!", "Aviso de Sucesso :)", 1, minionBanana);
                                recipeNameNR.setText("");
                                recipeCommentNR.setText("");
                                recipeIngredientsNR.setText("");
                                recipePreparationNR.setText("");
                            } catch (IOException ex) {
                                Logger.getLogger(Recipe_GUI.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                }
            }
        }
        else{
            ImageIcon minionTongue = new ImageIcon(TestOption.class.getResource("/recipes/minionTongue.png"));
            JOptionPane.showMessageDialog(null, "Selecione a categoria onde deseja criar a receita!", "Aviso de Insucesso :(", 1, minionTongue);
        }
    }//GEN-LAST:event_createRecipeNRActionPerformed

