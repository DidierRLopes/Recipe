    private void changeRecipeMRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeRecipeMRActionPerformed
        if(recipeListMR.getSelectedIndex() != -1 && categoryListMR.getSelectedIndex() != -1){
            
            int com = recipeCommentMR.getText().compareTo(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).commentRecipe);
            int ing = recipeIngredientsMR.getText().compareTo(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).ingredientsRecipe);
            int pre = recipePreparationMR.getText().compareTo(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).preparationRecipe);
            
            if(com == 0 && ing == 0 && pre == 0){
                ImageIcon minionEvilLol = new ImageIcon(TestOption.class.getResource("/recipes/minionEvilLol.png"));
                JOptionPane.showMessageDialog(null, "Não há nada para alterar. \nAltera primeiro um dos campos da receita: Comentário, Ingredientes ou Preparação!", "Aviso de Insucesso :(", 0, minionEvilLol);      
            }
            else{
                if(recipeCommentMR.getText().contains(";") || recipeIngredientsMR.getText().contains(";") || recipePreparationMR.getText().contains(";")){
                    ImageIcon minionHumf = new ImageIcon(TestOption.class.getResource("/recipes/minionHumf.png"));
                    JOptionPane.showMessageDialog(null, "Um dos campos tem presente o caracter \";\".\nDeve remover esse caracter!", "Aviso de Insucesso :(", 0, minionHumf);
                }
                else{
                    try {
                        String recName = Recipes.listOfRecipes(categoryListMR.getSelectedIndex()).get(recipeListMR.getSelectedIndex());
                        Object[] options = { "Sim", "Não" };
                        ImageIcon minionAmazed = new ImageIcon(TestOption.class.getResource("/recipes/minionAmazed.png"));
                        int option = JOptionPane.showOptionDialog(null, "Deseja realmente alterar a receita \"" + recName + "\"?", "Aviso de alteração permanente :/", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, minionAmazed, options, options[0]);

                        switch (option) {
                            case 0:
                                Recipes.changeRecipe(categoryListMR.getSelectedIndex(), recipeListMR.getSelectedIndex(), recipeCommentMR.getText(), recipeIngredientsMR.getText(), recipePreparationMR.getText());
                                Recipes.saveCategoryFile(categoryListMR.getSelectedIndex());
                                ImageIcon minionCake = new ImageIcon(TestOption.class.getResource("/recipes/minionCake.png"));
                                JOptionPane.showMessageDialog(null, "A receita \"" + recName + "\" foi alterada com sucesso!", "Aviso de Sucesso :)", 1, minionCake);
                                break;
                            case 1:
                                recipeCommentMR.setText(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).commentRecipe);
                                recipeIngredientsMR.setText(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).ingredientsRecipe);
                                recipePreparationMR.setText(Recipes.ALL.get(categoryListMR.getSelectedIndex()).recipesCategory.get(recipeListMR.getSelectedIndex()).preparationRecipe);
                        }
                    } catch (IOException ex) {
                        Logger.getLogger(Recipe_GUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        else 
            if(recipeListMR.getSelectedIndex() == -1 && categoryListMR.getSelectedIndex() != -1){
                ImageIcon minionGirl = new ImageIcon(TestOption.class.getResource("/recipes/minionGirl.png"));
                JOptionPane.showMessageDialog(null, "Selecione uma receita para ter algo para alterar!", "Aviso de Insucesso :(", 0, minionGirl);      
            }
            else{
                ImageIcon minionGru = new ImageIcon(TestOption.class.getResource("/recipes/minionGru.png"));
                JOptionPane.showMessageDialog(null, "Selecione uma categoria e uma receita para ter algo para alterar!", "Aviso de Insucesso :(", 0, minionGru);
            }    
    }//GEN-LAST:event_changeRecipeMRActionPerformed

